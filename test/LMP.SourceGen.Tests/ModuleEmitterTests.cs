        Assert.True(getPredictorsIdx > partialClassIdx, "GetPredictors should be inside class");
        Assert.True(classifyIdx > getPredictorsIdx, "classify entry should follow GetPredictors");
        Assert.True(draftIdx > classifyIdx, "draft entry should follow classify");
    }

