                new PredictorFieldModel("_draft", "global::Demo.ClassifyTicket", "global::Demo.DraftReply"),
            });

        var source = ModuleEmitter.GenerateSource(model);

        // Verify overall structure order
        var partialClassIdx = source.IndexOf("partial class TicketTriageModule");
        var getPredictorsIdx = source.IndexOf("public override IReadOnlyList<(string Name, IPredictor Predictor)> GetPredictors()");
