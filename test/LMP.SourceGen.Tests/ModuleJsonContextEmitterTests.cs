    #region Full snapshot-style verification

    [Fact]
    public void GenerateSource_FullOutput_MatchesExpectedStructure()
    {
        var model = CreateModuleModel(ns: "Demo", typeName: "TicketTriageModule");
        var source = ModuleJsonContextEmitter.GenerateSource(model);

        // Verify overall structure: header → usings → namespace → attributes → class
        var headerIdx = source.IndexOf("// <auto-generated />");
        var usingStjIdx = source.IndexOf("using System.Text.Json.Serialization;");
        var usingLmpIdx = source.IndexOf("using LMP;");
        var nsIdx = source.IndexOf("namespace Demo;");
        var genCodeIdx = source.IndexOf("[GeneratedCode(");
        var optionsIdx = source.IndexOf("[JsonSourceGenerationOptions(");
        var moduleStateIdx = source.IndexOf("[JsonSerializable(typeof(ModuleState))]");
        var predictorStateIdx = source.IndexOf("[JsonSerializable(typeof(PredictorState))]");
        var demoEntryIdx = source.IndexOf("[JsonSerializable(typeof(DemoEntry))]");
        var classIdx = source.IndexOf("file partial class TicketTriageModuleJsonContext : JsonSerializerContext;");

        Assert.True(headerIdx >= 0, "Auto-generated header not found");
        Assert.True(usingStjIdx > headerIdx, "STJ usings should follow header");
        Assert.True(usingLmpIdx > usingStjIdx, "LMP using should follow STJ usings");
        Assert.True(nsIdx > usingLmpIdx, "Namespace should follow usings");
        Assert.True(genCodeIdx > nsIdx, "GeneratedCode attribute should follow namespace");
        Assert.True(optionsIdx > genCodeIdx, "JsonSourceGenerationOptions should follow GeneratedCode");
        Assert.True(moduleStateIdx > optionsIdx, "ModuleState serializable should follow options");
        Assert.True(predictorStateIdx > moduleStateIdx, "PredictorState serializable should follow ModuleState");
        Assert.True(demoEntryIdx > predictorStateIdx, "DemoEntry serializable should follow PredictorState");
        Assert.True(classIdx > demoEntryIdx, "Class declaration should follow attributes");
    }

