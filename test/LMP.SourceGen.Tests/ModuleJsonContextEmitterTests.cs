    private static ModuleModel CreateModuleModel(
        string ns = "TestApp",
        string typeName = "TestModule",
        PredictorFieldModel[]? fields = null)
    {
        fields ??=
        [
            new PredictorFieldModel("_classify", "global::TestApp.TicketInput", "global::TestApp.ClassifyTicket"),
            new PredictorFieldModel("_draft", "global::TestApp.ClassifyTicket", "global::TestApp.DraftReply"),
        ];

        return new ModuleModel(
            Namespace: ns,
            TypeName: typeName,
            PredictorFields: new EquatableArray<PredictorFieldModel>(fields.ToImmutableArray()));
    }

