            using System.ComponentModel;
            using System.Threading.Tasks;

            namespace LMP
            {
                public class Predictor<TInput, TOutput> where TOutput : class
                {
                    public Predictor(object client) { }
                    public virtual Task<TOutput> PredictAsync(TInput input) => throw null!;
                    public string Instructions { get; set; } = "";
                    public string Name { get; set; } = "";
                }
                public class Trace { public void Record(string n, object i, object o) {} }
                public class LmpAssertionException : System.Exception { }
                public class LmpMaxRetriesExceededException : System.Exception
                {
                    public LmpMaxRetriesExceededException(string n, int r) { }
                }
                public interface IChatClient { }
            }

            namespace TestApp
            {
                using LMP;

                public record Input1(string Text);

                [LmpSignature("Test")]
                public partial record Output1
                {
                    [Description("Result")]
                    public required string Result { get; init; }
                }

                public class MyModule
                {
                    private readonly Predictor<Input1, Output1> _a = null!;
                    private readonly Predictor<Input1, Output1> _b = null!;

                    public async Task Run()
                    {
                        var r1 = await _a.PredictAsync(new Input1("hello"));
                        var r2 = await _b.PredictAsync(new Input1("world"));
                    }
                }
            }
            """;

        var (diagnostics, runResult) = RunGenerator(source);
        Assert.Empty(diagnostics);

