    [Fact]
    public void GenerateSource_MultipleCallSites_SingleMethod_MultipleAttributes()
    {
        var callSites = ImmutableArray.Create(
            CreateCallSite("global::T.In", "global::T.Out", "Out", "T", 1, "LOC1", "a.cs(1,1)"),
            CreateCallSite("global::T.In", "global::T.Out", "Out", "T", 1, "LOC2", "b.cs(2,2)"));
        var source = InterceptorEmitter.GenerateSource(callSites);

        // Both attributes on same method
        var methodIdx = source.IndexOf("PredictAsync_0(");
        var loc1Idx = source.IndexOf("[InterceptsLocation(1, \"LOC1\")]");
        var loc2Idx = source.IndexOf("[InterceptsLocation(1, \"LOC2\")]");

        Assert.True(loc1Idx < methodIdx, "First [InterceptsLocation] should precede method");
        Assert.True(loc2Idx < methodIdx, "Second [InterceptsLocation] should precede method");

        // Only one method emitted
        Assert.DoesNotContain("PredictAsync_1(", source);
    }

