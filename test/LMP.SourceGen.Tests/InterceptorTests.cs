    #region InterceptorExtractor — Syntax predicate tests

    [Fact]
    public void IsCandidate_ReturnsTrueForPredictAsyncInvocation()
    {
        var code = "var x = predictor.PredictAsync(input);";
        var tree = CSharpSyntaxTree.ParseText(code);
        var root = tree.GetRoot();
        var invocation = root.DescendantNodes()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.InvocationExpressionSyntax>()
            .First();

        Assert.True(InterceptorExtractor.IsCandidate(invocation, default));
    }

    [Fact]
    public void IsCandidate_ReturnsFalseForOtherMethods()
    {
        var code = "var x = predictor.ForwardAsync(input);";
        var tree = CSharpSyntaxTree.ParseText(code);
        var root = tree.GetRoot();
        var invocation = root.DescendantNodes()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.InvocationExpressionSyntax>()
            .First();

        Assert.False(InterceptorExtractor.IsCandidate(invocation, default));
    }

    [Fact]
    public void IsCandidate_ReturnsFalseForNonMemberAccess()
    {
        var code = "var x = PredictAsync(input);";
        var tree = CSharpSyntaxTree.ParseText(code);
        var root = tree.GetRoot();
        var invocation = root.DescendantNodes()
            .OfType<Microsoft.CodeAnalysis.CSharp.Syntax.InvocationExpressionSyntax>()
            .First();

        Assert.False(InterceptorExtractor.IsCandidate(invocation, default));
    }

