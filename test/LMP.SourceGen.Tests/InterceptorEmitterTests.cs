        Assert.Contains("[GeneratedCode(\"LMP.Generators\", \"1.0.0\")]", source);
    }

    #endregion

    #region Direct extractor tests — InterceptorExtractor.IsCandidate

    [Fact]
    public void IsCandidate_ReturnsFalse_ForNonInvocation()
    {
        var tree = CSharpSyntaxTree.ParseText("class C { int x; }");
        var root = tree.GetRoot();
        foreach (var node in root.DescendantNodes())
        {
            Assert.False(InterceptorExtractor.IsCandidate(node, default));
        }
    }

    [Fact]
    public void IsCandidate_ReturnsTrue_ForPredictAsyncInvocation()
    {
        var tree = CSharpSyntaxTree.ParseText("class C { void M() { x.PredictAsync(input); } }");
        var root = tree.GetRoot();
        var invocations = root.DescendantNodes()
            .Where(n => InterceptorExtractor.IsCandidate(n, default))
            .ToList();

        Assert.Single(invocations);
    }

    [Fact]
    public void IsCandidate_ReturnsFalse_ForOtherMethodCalls()
    {
        var tree = CSharpSyntaxTree.ParseText("class C { void M() { x.SomeOther(input); } }");
        var root = tree.GetRoot();
        var candidates = root.DescendantNodes()
            .Where(n => InterceptorExtractor.IsCandidate(n, default))
            .ToList();

        Assert.Empty(candidates);
    }

    [Fact]
    public void IsCandidate_ReturnsFalse_ForStaticPredictAsyncCall()
    {
        var tree = CSharpSyntaxTree.ParseText("class C { void M() { PredictAsync(input); } }");
        var root = tree.GetRoot();
        var candidates = root.DescendantNodes()
            .Where(n => InterceptorExtractor.IsCandidate(n, default))
            .ToList();

        // Static calls without member access should not match
        Assert.Empty(candidates);
    }

    #endregion

    #region PromptBuilder 4-param overload

    [Fact]
    public void PromptBuilder_EmitsBuildMessages_4ParamOverload()
    {
        var model = CreatePromptBuilderModel();
        var source = PromptBuilderEmitter.GenerateSource(model);

        Assert.Contains("public static IList<ChatMessage> BuildMessages(", source);
        Assert.Contains("string instructions,", source);
        Assert.Contains("string? lastError)", source);
    }

    [Fact]
    public void PromptBuilder_4ParamOverload_HandlesLastError()
    {
        var model = CreatePromptBuilderModel();
        var source = PromptBuilderEmitter.GenerateSource(model);

        Assert.Contains("if (lastError is not null)", source);
        Assert.Contains("Previous attempt failed:", source);
    }

    [Fact]
    public void PromptBuilder_2ParamOverload_DelegatesTo4Param()
    {
        var model = CreatePromptBuilderModel();
        var source = PromptBuilderEmitter.GenerateSource(model);

        Assert.Contains("return BuildMessages(Instructions, input, demos, null);", source);
    }

    #endregion

    #region Helpers

    private static ImmutableArray<InterceptorCallSiteModel> CreateSingleCallSite()
    {
        return ImmutableArray.Create(
            new InterceptorCallSiteModel(
                InputTypeFQN: "global::TestApp.TicketInput",
                OutputTypeFQN: "global::TestApp.ClassifyTicket",
                OutputTypeName: "ClassifyTicket",
                Namespace: "TestApp",
                LocationVersion: 1,
                LocationData: "testlocationdata",
                DisplayLocation: "test.cs(5,20)"));
    }

    private static PromptBuilderModel CreatePromptBuilderModel()
    {
        return new PromptBuilderModel(
            Namespace: "TestApp",
            OutputTypeName: "ClassifyTicket",
            InputTypeName: "TicketInput",
            InputTypeFullyQualified: "global::TestApp.TicketInput",
            OutputTypeFullyQualified: "global::TestApp.ClassifyTicket",
            Instructions: "Classify tickets",
            InputFields: new EquatableArray<InputFieldModel>(
                ImmutableArray.Create(new InputFieldModel("Text", "string", "The text"))),
            OutputFields: new EquatableArray<OutputFieldModel>(
                ImmutableArray.Create(new OutputFieldModel(
                    "Category", "string", "global::System.String", "The category",
                    IsRequired: false, IsNonSerializable: false, Location: default))));
    }

    private static int CountOccurrences(string source, string substring)
    {
        int count = 0;
        int idx = 0;
        while ((idx = source.IndexOf(substring, idx)) != -1)
        {
            count++;
            idx += substring.Length;
        }
        return count;
    }

    #endregion
}
