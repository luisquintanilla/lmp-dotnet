    [Fact]
    public void Pipeline_DuplicateCoTUsages_DeduplicatedByOutputType()
    {
        var source = """
            namespace LMP
            {
                [System.AttributeUsage(System.AttributeTargets.Class)]
                public sealed class LmpSignatureAttribute : System.Attribute
                {
                    public LmpSignatureAttribute(string instructions) { }
                }
                public class Predictor<TInput, TOutput> where TOutput : class
                {
                    public Predictor(object client) { }
                }
            }
            namespace LMP
            {
                public class ChainOfThought<TInput, TOutput> : Predictor<TInput, TOutput> where TOutput : class
                {
                    public ChainOfThought(object client) : base(client) { }
                }
            }

            namespace TestApp
            {
                using LMP;
                using System.ComponentModel;

                public record Input1(string Text);
                public record Input2(string Value);

                [LmpSignature("Classify")]
                public partial record ClassifyTicket
                {
                    [Description("Category")]
                    public required string Category { get; init; }
                }

                public partial class Module1
                {
                    private readonly ChainOfThought<Input1, ClassifyTicket> _cot1 = null!;
                }
                public partial class Module2
                {
                    private readonly ChainOfThought<Input2, ClassifyTicket> _cot2 = null!;
                }
            }
            """;

        var (_, runResult) = RunGenerator(source);

        var cotFiles = runResult.Results[0].GeneratedSources
            .Where(s => s.HintName.Contains("ChainOfThought.g.cs"))
            .ToList();

        // Should deduplicate — same TOut means only one file
        Assert.Single(cotFiles);
        Assert.Equal("ClassifyTicket.ChainOfThought.g.cs", cotFiles[0].HintName);
    }

