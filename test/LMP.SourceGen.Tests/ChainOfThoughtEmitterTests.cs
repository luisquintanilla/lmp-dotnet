    [Fact]
    public void GenerateSource_Snapshot_FullOutput()
    {
        var model = CreateModel(ns: "Demo", typeName: "ClassifyTicket");
        var source = ChainOfThoughtEmitter.GenerateSource(model);

        // Verify structure ordering: header → namespace → doc → record → JsonContext
        var nsIndex = source.IndexOf("namespace Demo;");
        var recordIndex = source.IndexOf("internal partial record ClassifyTicketWithReasoning");
        var reasoningIndex = source.IndexOf("public required string Reasoning");
        var categoryIndex = source.IndexOf("public required string Category");
        var urgencyIndex = source.IndexOf("public required int Urgency");
        var jsonCtxIndex = source.IndexOf("ClassifyTicketWithReasoningJsonOptions");

        Assert.True(nsIndex < recordIndex, "namespace before record");
        Assert.True(recordIndex < reasoningIndex, "record before Reasoning field");
        Assert.True(reasoningIndex < categoryIndex, "Reasoning before Category");
        Assert.True(categoryIndex < urgencyIndex, "Category before Urgency");
        Assert.True(urgencyIndex < jsonCtxIndex, "Urgency before JsonContext");
    }

