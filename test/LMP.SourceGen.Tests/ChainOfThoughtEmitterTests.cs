    private static ChainOfThoughtModel CreateModel(
        string ns = "TestApp",
        string typeName = "ClassifyTicket")
    {
        var fields = new[]
        {
            new OutputFieldModel("Category", "string", "global::System.String",
                "Category: billing, technical, account", true, false,
                LocationInfo.From(Location.None)),
            new OutputFieldModel("Urgency", "int", "global::System.Int32",
                "Urgency from 1 (low) to 5 (critical)", true, false,
                LocationInfo.From(Location.None)),
        };

        return new ChainOfThoughtModel(
            Namespace: ns,
            OutputTypeName: typeName,
            OutputFields: new EquatableArray<OutputFieldModel>(fields.ToImmutableArray()));
    }

    private static string GetCoTPipelineSource() => """
        namespace LMP
        {
            [System.AttributeUsage(System.AttributeTargets.Class)]
            public sealed class LmpSignatureAttribute : System.Attribute
            {
                public LmpSignatureAttribute(string instructions) { }
            }
            public class Predictor<TInput, TOutput> where TOutput : class
            {
                public Predictor(object client) { }
            }
        }
        namespace LMP
        {
            public class ChainOfThought<TInput, TOutput> : Predictor<TInput, TOutput> where TOutput : class
            {
                public ChainOfThought(object client) : base(client) { }
            }
        }

        namespace TestApp
        {
            using LMP;
            using System.ComponentModel;

            public record TicketInput(string TicketText);

            [LmpSignature("Classify a support ticket")]
            public partial record ClassifyTicket
            {
                [Description("Category: billing, technical, account")]
                public required string Category { get; init; }

                [Description("Urgency from 1 (low) to 5 (critical)")]
                public required int Urgency { get; init; }
            }

            public partial class MyModule
            {
                private readonly ChainOfThought<TicketInput, ClassifyTicket> _cot;

                public MyModule(object client)
                {
                    _cot = new ChainOfThought<TicketInput, ClassifyTicket>(client);
                }
            }
        }
        """;

    private static (ImmutableArray<Diagnostic> Diagnostics, GeneratorDriverRunResult RunResult) RunGenerator(
        string source)
    {
        var syntaxTree = CSharpSyntaxTree.ParseText(source);

        var references = new[]
        {
            MetadataReference.CreateFromFile(typeof(object).Assembly.Location),
            MetadataReference.CreateFromFile(typeof(System.ComponentModel.DescriptionAttribute).Assembly.Location),
        };

        // Also need System.Runtime for fundamental types
        var runtimePath = System.IO.Path.GetDirectoryName(typeof(object).Assembly.Location)!;
        var runtimeRef = MetadataReference.CreateFromFile(
            System.IO.Path.Combine(runtimePath, "System.Runtime.dll"));

        var compilation = CSharpCompilation.Create(
            "TestAssembly",
            new[] { syntaxTree },
            references.Append(runtimeRef),
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        var generator = new LmpSourceGenerator();
        GeneratorDriver driver = CSharpGeneratorDriver.Create(generator);

        driver = driver.RunGeneratorsAndUpdateCompilation(
            compilation, out var outputCompilation, out var diagnostics);

        var runResult = driver.GetRunResult();
        return (diagnostics, runResult);
    }

