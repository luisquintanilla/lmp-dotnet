    #region Full snapshot-style verification

    [Fact]
    public void GenerateSource_FullOutput_MatchesExpectedStructure()
    {
        var model = CreateFullModel();
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        // Verify overall structure by checking key sections appear in order
        var instructionsIdx = source.IndexOf("private const string Instructions =");
        var fieldDescIdx = source.IndexOf("private const string FieldDescriptions =");
        var defaultIdx = source.IndexOf("public static string DefaultInstructions =>");
        var buildIdx = source.IndexOf("public static IList<ChatMessage> BuildMessages(");
        var formatInputIdx = source.IndexOf("private static string FormatInput(");
        var formatOutputIdx = source.IndexOf("private static string FormatOutput(");

        Assert.True(instructionsIdx > 0, "Instructions constant not found");
        Assert.True(fieldDescIdx > instructionsIdx, "FieldDescriptions should follow Instructions");
        Assert.True(defaultIdx > fieldDescIdx, "DefaultInstructions should follow FieldDescriptions");
        Assert.True(buildIdx > defaultIdx, "BuildMessages should follow DefaultInstructions");
        Assert.True(formatInputIdx > buildIdx, "FormatInput should follow BuildMessages");
        Assert.True(formatOutputIdx > formatInputIdx, "FormatOutput should follow FormatInput");
    }

