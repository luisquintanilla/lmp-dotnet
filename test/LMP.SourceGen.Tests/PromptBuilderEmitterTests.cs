    #region Generated code structure

    [Fact]
    public void GenerateSource_IncludesAutoGeneratedHeader()
    {
        var model = CreateFullModel();
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.StartsWith("// <auto-generated />", source);
        Assert.Contains("#nullable enable", source);
    }

    [Fact]
    public void GenerateSource_IncludesRequiredUsings()
    {
        var model = CreateFullModel();
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("using System;", source);
        Assert.Contains("using System.CodeDom.Compiler;", source);
        Assert.Contains("using System.Collections.Generic;", source);
        Assert.Contains("using Microsoft.Extensions.AI;", source);
    }

    [Fact]
    public void GenerateSource_IncludesNamespace()
    {
        var model = CreateFullModel(ns: "TestApp.Models");
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("namespace TestApp.Models;", source);
    }

    [Fact]
    public void GenerateSource_OmitsNamespace_WhenGlobal()
    {
        var model = CreateFullModel(ns: "");
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.DoesNotContain("namespace", source);
    }

    [Fact]
    public void GenerateSource_UsesFileStaticClass()
    {
        var model = CreateFullModel(typeName: "ClassifyTicket");
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("file static class ClassifyTicketPromptBuilder", source);
    }

    [Fact]
    public void GenerateSource_IncludesGeneratedCodeAttribute()
    {
        var model = CreateFullModel();
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("[GeneratedCode(\"LMP.Generators\", \"1.0.0\")]", source);
    }

    #endregion

    #region Instructions and FieldDescriptions

    [Fact]
    public void GenerateSource_EmitsInstructionsConstant()
    {
        var model = CreateFullModel(instructions: "Classify a support ticket by category and urgency");
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("private const string Instructions =", source);
        Assert.Contains("Classify a support ticket by category and urgency", source);
    }

    [Fact]
    public void GenerateSource_EscapesSpecialCharactersInInstructions()
    {
        var model = CreateFullModel(instructions: "Use \"quotes\" and \\backslash");
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("Use \\\"quotes\\\" and \\\\backslash", source);
    }

    [Fact]
    public void GenerateSource_EmitsFieldDescriptions_WithInputAndOutputFields()
    {
        var model = CreateFullModel();
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.Contains("private const string FieldDescriptions =", source);
        Assert.Contains("Input Fields:", source);
        Assert.Contains("- TicketText (string): The raw ticket text", source);
        Assert.Contains("- AccountTier (string): Customer plan tier", source);
        Assert.Contains("Output Fields:", source);
        Assert.Contains("- Category (string): Category: billing, technical, account", source);
        Assert.Contains("- Urgency (int): Urgency from 1 (low) to 5 (critical)", source);
    }

    [Fact]
    public void GenerateSource_EmitsFieldDescriptions_OmitsDescriptionWhenNull()
    {
        var model = CreateModel(
            inputTypeName: "MyInput",
            inputFields: new[] { new InputFieldModel("Field1", "string", null) },
            outputFields: new[] { CreateOutputField("Field2", "int", null) });
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        // Field without description has no colon separator
        Assert.Contains("- Field1 (string)", source);
        Assert.DoesNotContain("- Field1 (string):", source);
        Assert.Contains("- Field2 (int)", source);
        Assert.DoesNotContain("- Field2 (int):", source);
    }

    [Fact]
    public void GenerateSource_EmitsFieldDescriptions_WithOnlyOutputFields()
    {
        var model = CreateModel(
            inputTypeName: "EmptyInput",
            inputFields: System.Array.Empty<InputFieldModel>(),
            outputFields: new[] { CreateOutputField("Result", "string", "The result") });
        var source = PromptBuilderEmitter.GenerateSource(model)!;

        Assert.DoesNotContain("Input Fields:", source);
        Assert.Contains("Output Fields:", source);
        Assert.Contains("- Result (string): The result", source);
    }

    #endregion

    #region DefaultInstructions

