{
    #region GenerateSource — null/skip cases

    [Fact]
    public void GenerateSource_ReturnsNull_WhenInputTypeNameIsNull()
    {
        var model = CreateModel(inputTypeName: null);
        var source = PromptBuilderEmitter.GenerateSource(model);
        Assert.Null(source);
    }

