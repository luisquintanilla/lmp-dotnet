    #region Helpers

    private static OutputTypeModel CreateFullModel(
        string ns = "TestApp",
        string typeName = "ClassifyTicket",
        string instructions = "Classify a support ticket by category and urgency",
        string inputTypeName = "TicketInput")
    {
        var inputFields = new[]
        {
            new InputFieldModel("TicketText", "string", "The raw ticket text"),
            new InputFieldModel("AccountTier", "string", "Customer plan tier"),
        };

        var outputFields = new[]
        {
            CreateOutputField("Category", "string", "Category: billing, technical, account", isRequired: true),
            CreateOutputField("Urgency", "int", "Urgency from 1 (low) to 5 (critical)", isRequired: true),
        };

        return new OutputTypeModel(
            Namespace: ns,
            TypeName: typeName,
            Instructions: instructions,
            InputFields: new EquatableArray<InputFieldModel>(inputFields.ToImmutableArray()),
            OutputFields: new EquatableArray<OutputFieldModel>(outputFields.ToImmutableArray()),
            IsPartialRecord: true,
            TypeKindDescription: null,
            Location: default)
        {
            InputTypeName = inputTypeName,
        };
    }

    private static OutputTypeModel CreateModel(
        string? inputTypeName,
        InputFieldModel[]? inputFields = null,
        OutputFieldModel[]? outputFields = null,
        string ns = "TestApp",
        string typeName = "TestOutput",
        string instructions = "Test instructions")
    {
        return new OutputTypeModel(
            Namespace: ns,
            TypeName: typeName,
            Instructions: instructions,
            InputFields: new EquatableArray<InputFieldModel>(
                (inputFields ?? System.Array.Empty<InputFieldModel>()).ToImmutableArray()),
            OutputFields: new EquatableArray<OutputFieldModel>(
                (outputFields ?? System.Array.Empty<OutputFieldModel>()).ToImmutableArray()),
            IsPartialRecord: true,
            TypeKindDescription: null,
            Location: default)
        {
            InputTypeName = inputTypeName,
        };
    }

    private static OutputFieldModel CreateOutputField(
        string name, string clrType, string? description, bool isRequired = false)
    {
        return new OutputFieldModel(
            Name: name,
            ClrTypeName: clrType,
            FullyQualifiedTypeName: "global::" + clrType,
            Description: description,
            IsRequired: isRequired,
            Location: default);
    }

    #endregion
